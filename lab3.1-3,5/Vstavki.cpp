#include <iostream> // Стандартная библиотека ввода-вывода

// Функция сортировки вставками
void insertionSort(int arr[], int n) {
    for (int i = 1; i < n; ++i) {                       // Начинаем обход массива со второго элемента
        int key = arr[i];                               // Элемент, который будем вставлять на нужное место
        int j = i - 1;                                  // Предыдущий элемент относительно ключа
        
        // Двигаемся назад, пока ключ меньше предыдущего элемента
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];                        // Смещаем больший элемент вперед
            j--;                                        // Переходим к предыдущему элементу
        }
        
        arr[j + 1] = key;                               // Вставляем ключ на свободное место
    }
}

// Функция для вывода массива
void printArray(const int arr[], int n) {
    for (int i = 0; i < n; ++i) {
        std::cout << arr[i] << " ";
    }
    std::cout << std::endl;
}

// Основная программа
int main() {
    int arr[] = {64, 34, 25, 12, 22, 11, 90};          // Исходный массив
    int n = sizeof(arr)/sizeof(arr[0]);                 // Вычисляем размер массива
    
    std::cout << "Исходный массив: ";                   // Выводим исходный массив
    printArray(arr, n);
    
    insertionSort(arr, n);                              // Сортируем массив
    
    std::cout << "Отсортированный массив: ";           // Выводим отсортированный массив
    printArray(arr, n);
    
    return 0;
}